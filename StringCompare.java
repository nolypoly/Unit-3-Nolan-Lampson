public class StringCompare {
  public static void main (String[] args) {
    String word1 = "able";
    String word2 = "Baker";
    System.out.println(word1.compareTo(word2));
    String word3 = "Its";
    String word4 = "It's";
    System.out.println(word3.compareTo(word4));
    String word5 = "horse";
    String word6 = "horsefly";
    System.out.println(word5.compareTo(word6));
  }
}
